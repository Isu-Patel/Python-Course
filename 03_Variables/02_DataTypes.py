# Python supports several built-in data types:
#          Integers (int): Whole numbers (e.g., 10, -5).
#          Floats (float): Decimal numbers (e.g., 3.14, -0.5).
#          Strings (str): Textual data enclosed in quotes(e.g., "Hello", "Python").
#          Booleans (bool): Represents True or False values.
#          Lists: Ordered collections of items (e.g., [1, 2, 3]).
#          Tuples: Immutable ordered collections (e.g., (1, 2, 3)).
#          Sets: Unordered collections of unique items (e.g., {1, 2, 3}).
#          Dictionaries: Key-value pairs (e.g., {"name": "Isu", "age": 14}).
#          None: Represents the absence of a value or a null value.

age = 3
print(age)
print(type(age))

cgpa = 8.2
print(cgpa)
print(type(cgpa))

name = "Isu"
print(name)
print(type(name))

is_completed = True # Can be True or False
print(is_completed)
print(type(is_completed))